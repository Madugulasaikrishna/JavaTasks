//Code For Relational Operators

import java.io.*;
class RelationalOperators{
    public static void main(String args[]){
        int a=10, b=20, c=30;
        System.out.println("> is "+ (a>b));
        System.out.println("> is "+ (a>c));
        System.out.println("< is "+ (a<b));
        System.out.println("< is "+ (a<c));
        System.out.println(">= is "+ (a>=b));
        System.out.println(">= is "+ (a>=c));
        System.out.println("<= is "+ (a<=b));
        System.out.println("<= is "+ (a<=c));
        System.out.println("== is "+ (a==b));
        System.out.println("== is "+ (a==c));
        System.out.println("!= is "+ (a!=b));
        System.out.println("!= is "+ (a!=c));
    }
}